public class Operador2 {
	
	public static void main(String[] args){
		
		int total = 10;
		total += 3;
		System.out.println("Total: " + total); 
		total -= 2;
		System.out.println("Total: " + total);
		total *= 3;
		System.out.println("Total: " + total);
		total /= 3;
		System.out.println("Total: " + total);
		total %= 5;
		System.out.println("Total: " + total);
		
		
		
	}
	
	
	
}